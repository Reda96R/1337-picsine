#include <unistd.h>

void	ft_print_numbers(void)

{
	char	x;
	char	y;

	x = '0';
	y = '9';
	while (x <= y)
	{	
		write(1, &x, 1);
		x++;
	}
}

int	main(void)
{
	ft_print_numbers();
}
