int	ft_sqrt(int nb)
{
	int	n;

	n = 1;
	if (nb < 0 || nb == 0)
		return (0);
	else if (nb == 1)
		return (1);
	while (nb / n >= n)
	{
		if (n * n == nb)
			return (n);
		n++;
	}
	return (0);
}
