int	ft_atoi(char *str)
{
	int	i;
	int	j;
	int	r;

	i = 0;
	j = 0;
	r = 0;
	while ((str[i] >= 9 && str[i] <= 13 ) || str[i] == 32)
		i++;
	while (str[i] == 45 || str[i] == 43 )
	{
		if (str[i] == 45)
			j++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + str[i] - 48;
		i++;
	}
	if (j % 2 != 0)
		return (r * -1);
	return (r);
}
